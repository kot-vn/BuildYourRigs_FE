            </center>
            <br>
            <br>
            <p>Your case is just what it sounds like. It’s a metal box. It might be covered in glass panels
              and etched aluminum, but inside it’s just a big metal box that holds everything together.
              Make sure you match it up with your motherboard size. For example, if you have an ATX
              motherboard, you need an ATX (or “full-size”) case.</p>
            <br>
