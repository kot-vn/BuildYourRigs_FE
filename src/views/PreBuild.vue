            <br>
            <h2>Processor (CPU)</h2>
            <br>
            <center>
              <img src="../assets/images/Gear-AMD-Ryzen-5900x.png" alt="">
            </center>
            <br>
            <br>
            <p>This is the brain of your computer. It sockets directly into the motherboard, and it’s the
              single most important component of your PC. That doesn’t mean it has to be the most
              expensive (we’ll get to that later). If the CPU doesn't mention including thermal paste, get
              some. Don't eat your CPU. I know it looks tasty, but it's not actually food. </p>
            <br>
            <p>
              <b>Buying advice: </b>
              You're going to see the highest markups on your CPU and GPU. But these prices
              will move around quite a bit, so keep checking back if you can't afford the component you
              want at the moment.
            </p>
            <br>
            <hr>
            <br>
            <h2>Graphics Card (GPU)</h2>
            <br>
            <center>
              <img src="../assets/images/Gear-Nvidia-RTX-3050.png" alt="">
            </center>
            <br>
            <br>
            <p>If you’ll be playing games on this PC, you’ll need a graphics processing unit (also called a
              graphics card). This is a specialized processor that’s designed and optimized for handling
              visual data like the graphics in games. It's also used in video and photo editing and other
              graphics-intensive tasks. </p>
            <br>
            <p>
              <b>Buying advice: </b>
              These cards are tough to find in stock (or at a reasonable price) at the moment, so you may
              have to wait a while. Graphics cards are among the most in-demand PC components right now,
              and their prices are way higher than they should be. That's why the picks on this list are
              generally mid- to high-end. If you're going to pay through the nose for a graphics card,
              make sure it's a good one.
            </p>
            <br>
            <hr>
            <br>
            <h2>Storage</h2>
            <br>
            <center>
              <img src="../assets/images/Gear-WD-Black-M2-drive.png" alt="">
            </center>
            <br>
            <br>
            <p>This is your PC’s walk-in closet. This is where you store all your files, your games, your
              movies, your documents, your photos, your everything. You can always add more storage later.
            </p>
            <br>
            <hr>
            <br>
            <h2>Memory (RAM)</h2>
            <br>
            <center>
              <img src="../assets/images/G-Skills-Ripjaws-V-Gear.png" alt="">
            </center>
            <br>
            <br>
            <p>You’ll see a lot of the same terms when you’re looking at memory and storage, but they’re
              very different. Memory is more like that one table you toss things on to deal with later.
              It’s scratch paper; it’s short-term. It’s very important, though, because software uses
              memory to cache (temporarily store) data in a place where it can be retrieved quickly.
            </p>
            <br>
            <hr>
            <br>
            <h2>Power Supply (PSU)</h2>
            <br>
            <center>
              <img src="../assets/images/EVGA-PSU-Gear.png" alt="">
            </center>
            <br>
            <br>
            <p>Your power supply unit is a little box that keeps the electricity running to every component.
              It determines how quick and powerful your PC can be. The faster it is, the more power it
              needs, and you always want to have a little more than you need, just in case. Just like
              GPUs, PSUs are also in and out of stock right now.</p>
