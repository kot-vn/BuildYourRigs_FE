            <br>
            <h2>Putting It All Together</h2>
            <br>
            <center>
              <img src="../assets/images/Build-a-PC-Guide-1249969209.png" alt="">
            </center>
            <br>
            <br>
            <p>The internal layout of every gaming PC is a little different, so we're not going to get too
              far into the weeds here. Your best bet for specific instructions for your hardware will be
              referencing your manuals and searching for your components on YouTube. It can be super
              helpful to actually see a person handle and install your exact hardware, especially when
              you're stuck and can't quite figure out what the hell your manual is talking about. But here
              are some general tips for putting all these components together.
            </p>
            <br>
            <p>First, prep yourself a clean workspace. This can be a dining room table, a cleared-off
              desk—just any surface big enough for your case to lay flat on its side, with ample room
              around it for the rest of your components. You’ll also need a Phillips-head screwdriver that
              will fit the screws on your case. When you put these parts together, be sure to discharge
              any static buildup and work on a nonmetallic surface like a wooden table. Or you could just
              assemble the motherboard on top of the cardboard box it comes in.</p>
            <br>
            <p>Most of the components you bought are going to come with instruction manuals; keep them
              handy. We’re going to start with the motherboard, so open up the instruction manual to the
              installation page. It can be pretty intimidating—there’s a lot to look at—but think of all
              this as a big Lego set. Each piece fits into each other piece. For the motherboard, your
              first job is going to be seating your CPU.
            </p>
            <br>
            <h3>Installing Your CPU</h3>
            <br>
            <p>Depending on what kind of CPU you purchased (Intel or AMD), the chip will have either little
              prongs on one side (don’t touch them) or little golden contacts on one side (don’t touch
              them). Seriously, don’t touch that side of your chip. Oils from your fingertips can damage
              the contacts, or you might bend a pin. Do either one and your processor becomes nothing more
              than an expensive hunk of silicon.
            </p>
            <br>
            <p>Seating your processor is pretty easy. First, double-check your motherboard’s instructions
              and make sure you’ve unlocked the processor socket. It’ll be a big square with a bunch of
              little holes (or contacts), with a lever or button beside it. Your motherboard’s
              instructions will say explicitly how to unlock the socket so you can put your processor in
              without any issues.
            </p>
            <br>
            <p>Once you’ve confirmed that it’s unlocked and ready, just find which corner of your processor
              has a little golden triangle and line it up with the same symbol on your motherboard’s
              processor socket. Gently lower the processor into the socket, then gently flip the latch or
              locking mechanism. You shouldn’t have to fight it. If you have to press really hard,
              double-check that the processor is socketed correctly.</p>
            <br>
            <p>Next, you’re going to need your thermal paste. That little tiny plastic syringe of silvery
              goo is very important for this next step. Now that your processor is seated, take a look at
              the shiny square of silicon in the center of it. That’s where your heat sink is going to
              sit. Your processor came with a heat sink, and on one side of it, you’ll see a copper
              circle. You’re going to be putting the heat sink directly on top of the processor after we
              apply the thermal paste, with the silicone square and the copper circle lining up perfectly.
            </p>
            <br>
            <p>Go ahead and carefully squeeze a tiny ball (no bigger than a pea) of thermal paste onto the
              silicon square on your processor. You’ll want it as close to the center as you can get.
            </p>
            <br>
            <p>Now line up your heat sink with the screws surrounding your processor, and gently lower it
              into place. You’re gonna squish the thermal paste, and the goal here is to create a thin
              layer covering the back of your processor. It’s OK if it oozes a little bit, but if it oozes
              out and over the edge of the processor, you used too much. Get some isopropyl alcohol, dab
              it on a lint-free wipe, and wipe the processor and heat sink. Wait till they’re thoroughly
              dry and try again.</p>
            <br>
            <p>If it looks all right, screw your heat sink into place. Flip back to your motherboard
              instruction book and find the right place near the processor socket to plug in your heat
              sink’s cooling fan. It should be very close to your processor socket. Once you’ve found it,
              plug it in—congratulations, you just installed a CPU. This was the hardest part, and it’s
              over. Good job!
            </p>
            <br>
            <h3>Installing Your Storage and Memory</h3>
            <br>
            <p>Memory is maybe the easiest thing to install. See those vertical little sockets beside the
              CPU? Line up your sticks of RAM and slot them in, starting from the left-hand slot. They’ll
              lock into place once you’ve seated them properly. If you have two sticks of RAM, make sure
              to skip a slot between them. Your motherboard manual should say which slots to use.</p>
            <br>
            <p>For your hard drive or solid-state drive (SSD), find an empty bay in the front-facing part of
              your case. Slide your drive in and screw it into place. If you have an M.2 drive (a tiny SSD
              about the size of a stick of gum), there should be a place on the motherboard where you slot
              it in directly. Check out your motherboard’s manual to see where the M.2 slot is if you
              can't find it.</p>
            <br>
            <h3>Installing Your Motherboard and Power Supply</h3>
            <br>
            <p>The rest of this is formulaic. Start by putting your motherboard into your case. Consult your
              motherboard’s instructions, line up the screw holes in the case with the ones on your
              motherboard, and get to work.</p>
            <br>
            <p>Next, you’ll want to install your power supply. There should be a spot for it near the top or
              bottom of the case, a big square spot that will fit your supply perfectly. If you’re having
              trouble finding it, look at the back of your case: There’ll be a big empty square. That’s
              where the power supply goes (and where you’ll plug in your PC when you’re all done). Once
              you've found its home, slot it in and screw it into place.
            </p>
            <br>
            <p>Make sure all the snaky cables coming out of the power supply will reach your motherboard
              with room to spare. Don’t plug in anything yet; we’re going to come back to the power supply
              in a bit.</p>
            <br>
            <h2>Installing Your Graphics Card</h2>
            <br>
            <p>Your GPU is going to be pretty big. Even a modestly powerful GPU like the GTX 1060 is large
              compared to your other components. That means how it fits into your case is important. Once
              you put your GPU in there, space is going to start getting tight.</p>
            <br>
            <p>
              Flip open your motherboard’s instruction book again and look for a PCIe slot. It’s going to
              be a horizontal slot with a little plastic latch beside it, near the middle or bottom of
              your motherboard. That’s where the GPU plugs in. All you need to do is identify the back of
              your GPU (the side with the HDMI and DisplayPorts), line that up with the back of your case,
              and push the GPU into the horizontal slot. It should lock into place easily enough; if it
              doesn’t, make sure you’re inserting it correctly.
            </p>
            <br>
            <p>Find another one of those tiny little screws and fasten your GPU to the case. There’s a
              little spot for that on the same piece of metal with the HDMI ports. It should be easy to
              find.
            </p>
            <br>
            <p>Now, take a look at the cables coming out of your power supply. There should be a few that
              look like they could fit into the square (or rectangular) socket on the side of your GPU. It
              should look like six or eight little holes in a rectangle shape. If you’re having trouble,
              take a look at this video from hardware manufacturer Asus. Some of the specifics will be
              different, but it’s a great look at how to install a GPU.
            </p>
            <br>
            <h3>Ribbon Cables</h3>
            <br>
            <p>The motherboard needs to be hooked into all your devices. The power supply unit I used in
              this build is what's called fully modular, which means that you can select the cables you
              need and leave the rest off to eliminate clutter. Otherwise, power supplies have a ton of
              cables, and you'll have to deal with the unused power connections dangling inside your case.
              You'll need to connect the PSU to the SSD and the motherboard.</p>
            <br>
            <p>You also need to plug the motherboard into your case—the power buttons, audio plugs, and USB
              ports on the front of your case. There are special headers for each kind of plug scattered
              around the board, so you'll want to check your manual for the location and function of each
              grouping of pins. These tiny pins need to be plugged in a certain way, and they're
              unbelievably minuscule. There's also a hookup for the case's fan—in the case I used there
              was one header on the motherboard but three fans installed. Then there's the SATA cable for
              your SSD, which plugs into the motherboard.</p>
            <br>
            <p>This part of your build really depends on the hardware you purchased, so consult the manuals
              for each component to ensure you've correctly plugged it into your motherboard and the power
              supply.
            </p>
            <br>
            <h3>Boot It Up and Install Windows</h3>
            <br>
            <p>The final stage of your build is a simple one: Hit your power button. If the PC whirs to
              life, you probably put it together perfectly! If it doesn't, don't despair. There are a lot
              of potential problems that could cause a PC to fail to boot up for the first time. This
              video from Kingston goes over some pitfalls that might cause you some headaches, so if
              you're not able to boot your PC, give it a watch and retrace your steps.</p>
            <br>
            <p>
              There's also a chance you could have received faulty components. This video goes over some
              tips on how to check your parts. In general, if you’re having trouble with a specific
              component, YouTube is your friend. There are tons of helpful PC-building tutorials.
            </p>
            <br>
            <p>If it started up just fine, the next step is super easy: Turn it off. Remember that Windows
              flash drive you made earlier? Plug it into the PC and boot it up again. If you set it up
              right, your computer should just do its thing and get started installing Windows. If not,
              you might need to open your BIOS (check your motherboard's manual for how to do that) and
              set the USB drive to be a “boot device” first. Here's a brief rundown of that process (start
              at step 3).</p>
            <br>
            <hr>
