            <h2>First, Let’s Talk Shortages</h2>
            <br />
            <p>For several years now we've been in the midst of a PC hardware shortage. First,
              cryptocurrency miners bought up all the GPUs to farm crypto, then we got hit by a worldwide
              silicon shortage, and then there was that whole pandemic thing. Point is, it's tough to get
              all the parts you need to build a PC, it has been for years, and it's likely to continue
              being tough for the foreseeable future. I can't fix a global logistics crisis, but I can
              give you some pointers on shopping during one.</p>
            <br>
            <hr />
            <br>
            <h2>Third-Party Markup</h2>
            <br>
            <p>When you're shopping for components, make sure you're buying from a reputable vendor. You may
              think you're buying from a large online store like Amazon, Walmart, or Newegg, but they
              allow third-party sellers to list products as well. There's usually a little notice that
              will tell you as much, right underneath the buy button. </p>
            <br>
            <p>If it's shipped and sold from someone who isn't Amazon, Walmart, or Newegg, you're probably
              going to pay more for the component than its retail price. For instance, our top motherboard
              pick retails for $130, but as of this writing its listed on Newegg for $205. It's shipped
              and sold from a third-party retailer, hence the markup. First-party sales (when the product
              is shipped and sold by the store whose site you're visiting) tend to have better prices that
              hew closer to the actual manufacturer suggested retail price (MSRP) of the item.</p>
            <br>
            <p>We've tried to stick to first-party sales, but sometimes components are just not available
              through anyone but a third party. In that case, my advice is don't spend more than 50
              percent over the MSRP on any of these components. Give yourself a hard budget and please
              don't pay double or triple what any of these components are worth.</p>
            <br>
            <p>We've added buying advice to each category most affected by the shortage to speak more
              specifically to each component's scarcity, but in general just know that building a PC right
              now is probably going to cost you more than you'd expect.</p>
            <br>
            <hr />
            <br>
            <h2>What Do You Need?</h2>
            <br>
            <p>In order to get a list of components together, no matter what your experience level is, you
              should use PCPartPicker. Not only does it have everything you need to buy, it also lets you
              build your PC piece by piece right on the website and makes sure all your hardware will play
              nicely together. It even has a few example builds you can tweak to your liking.</p>
            <br>
            <p>Regardless of what kind of PC you’re building (home office or gaming), the components you
              need are going to be the same. You’ll need a motherboard, a central processing unit (CPU),
              storage, memory, a power supply, a case, and a monitor. The only thing you might not need if
              you're mostly using this PC for home-office tasks is a GPU (graphics processing unit), but
              it's necessary for photo or video editing and gaming. That’s a lot of stuff! Here's a little
              breakdown of what each component does, along with some hardware recommendations. </p>
            <br>
            <hr />
            <br>
            <center>
              <img src="../assets/images/Gigabyte-Motherboard.png" alt="">
            </center>
            <br>
            <br>
            <p>Every other component plugs into this circuit board. It’s the highway they use to communicate
              and collaborate. They come in different sizes and configurations, and each one looks a
              little different, but they all fill the same function. Make sure you know which processor
              you want before you buy a motherboard.</p>
            <br>
            <p>Motherboards come in a couple of flavors, but the most important thing to know is what kind
              of socket it has. There are basically two: LGA and AM. You'll always see them listed with a
              number after them, like “LGA1150” or “AM3.” The exact numbers after the LGA and AM portions
              of these socket names will change over time, to indicate which generation of Intel or AMD
              chips they support, but the current standards as of 2022 (which will work with the latest
              chips from either maker) are LGA1200 for Intel and AM4 for AMD.</p>
            <br>
            <p>Motherboards also come in a couple of sizes, the most common being ATX (or “full size”).
              That’s what I generally recommend, especially if this is your first build. Your PC case will
              list which size motherboard it supports, so make sure they match up.</p>
            <br>
            <p>
              <b>Buying advice: </b>
              Motherboards are less scarce than the other components on this list. We try
              to keep this list updated regularly with prices that aren't too much higher than MSRP, but
              if any of the prices here skyrockets between updates, shop around. Your chances of finding a
              reasonably priced motherboard are much better than your chances of finding a good price on a
              GPU.
            </p>
