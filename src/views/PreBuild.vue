            <p>Congratulations on building your first PC. It's a bit of a pain, but it's a great way to
              spend an afternoon. Or a couple of days, depending on how many unforeseen headaches you run
              into. Seeing as the pandemic hasn’t totally gone away, you can use your new PC to help you
              spend all those extra indoor hours productively (or just grinding out loot in Warframe).</p>
